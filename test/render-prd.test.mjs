import { test, describe } from "node:test";
import assert from "node:assert/strict";
import { renderPrd } from "../dist/render-prd.js";
import { validSpec, minimalValidSpec, clone } from "./fixtures.mjs";

describe("renderPrd - top-level structure", () => {
  test("contains title and byline", () => {
    const md = renderPrd(validSpec());
    assert.match(md, /# TaskFlow/);
    assert.match(md, /_Spec generated by draftlytic-mcp_/);
  });

  test("section headings appear in the expected order", () => {
    const md = renderPrd(validSpec());
    const headings = [
      "## Overview",
      "## Target Audience",
      "## Platforms",
      "## Tech Stack",
      "## Features",
      "## Constraints",
      "## Non-Goals",
      "## Revenue Model",
    ];
    const indexes = headings.map((h) => md.indexOf(h));
    for (const [i, idx] of indexes.entries()) {
      assert.notEqual(idx, -1, `expected to find heading: ${headings[i]}`);
    }
    for (let i = 1; i < indexes.length; i++) {
      assert.ok(
        indexes[i - 1] < indexes[i],
        `expected "${headings[i - 1]}" (at ${indexes[i - 1]}) to come before "${headings[i]}" (at ${indexes[i]})`,
      );
    }
  });

  test("title uses spec.name is placed before Overview section", () => {
    const spec = clone(validSpec());
    spec.name = "MyApp";
    const md = renderPrd(spec);
    assert.ok(md.startsWith("# MyApp\n"));
    assert.ok(md.indexOf("# MyApp") < md.indexOf("## Overview"));
  });

  test("overview and target_audience text appear under their headings", () => {
    const md = renderPrd(validSpec());
    assert.match(
      md,
      /## Overview\n\nA lightweight kanban board for solo developers to track side projects\./,
    );
    assert.match(
      md,
      /## Target Audience\n\nIndie developers juggling multiple side projects\./,
    );
  });
});

describe("renderPrd - features grouped by priority", () => {
  test("all three groups render in order must-have -> nice-to-have -> future", () => {
    const md = renderPrd(validSpec());
    const mustIdx = md.indexOf("### Must Have");
    const niceIdx = md.indexOf("### Nice to Have");
    const futureIdx = md.indexOf("### Future");
    assert.notEqual(mustIdx, -1);
    assert.notEqual(niceIdx, -1);
    assert.notEqual(futureIdx, -1);
    assert.ok(mustIdx < niceIdx);
    assert.ok(niceIdx < futureIdx);
  });

  test("numbering resets per group starting at #### 1.", () => {
    const md = renderPrd(validSpec());
    // Must Have group's first (and only) feature is numbered 1.
    assert.match(md, /### Must Have\n\n#### 1\. Drag-and-drop board/);
    // Nice to Have group's first feature also numbered 1.
    assert.match(md, /### Nice to Have\n\n#### 1\. Dark mode/);
    // Future group's first feature also numbered 1.
    assert.match(md, /### Future\n\n#### 1\. Mobile app/);
  });

  test("numbering increments within a group with multiple features of same priority", () => {
    const spec = clone(minimalValidSpec());
    spec.features = [
      { title: "First", description: "d1", priority: "must-have" },
      { title: "Second", description: "d2", priority: "must-have" },
      { title: "Third", description: "d3", priority: "must-have" },
    ];
    const md = renderPrd(spec);
    assert.match(md, /#### 1\. First/);
    assert.match(md, /#### 2\. Second/);
    assert.match(md, /#### 3\. Third/);
  });

  test("a priority group with zero features is omitted entirely", () => {
    const spec = clone(validSpec());
    spec.features = spec.features.filter((f) => f.priority !== "future");
    const md = renderPrd(spec);
    assert.ok(!md.includes("### Future"));
    assert.ok(md.includes("### Must Have"));
    assert.ok(md.includes("### Nice to Have"));
  });

  test("only must-have present -> only that subheading appears", () => {
    const spec = clone(minimalValidSpec());
    spec.features = [
      { title: "Solo", description: "Only feature.", priority: "must-have" },
    ];
    const md = renderPrd(spec);
    assert.ok(md.includes("### Must Have"));
    assert.ok(!md.includes("### Nice to Have"));
    assert.ok(!md.includes("### Future"));
  });

  test("feature description text is rendered under the numbered heading", () => {
    const md = renderPrd(validSpec());
    assert.match(
      md,
      /#### 1\. Drag-and-drop board\n\nMove cards between columns to update status\./,
    );
  });
});

