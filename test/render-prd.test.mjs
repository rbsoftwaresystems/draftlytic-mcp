describe("renderPrd - escapeCell", () => {
  test("pipes in screen name/purpose are escaped and newlines collapse", () => {
    const spec = clone(validSpec());
    spec.screens = [
      { name: "Weird | Screen\nName", purpose: "Does A | B\r\nand C\rD" },
    ];
    const md = renderPrd(spec);
    // Find the rendered row.
    const rowMatch = md.match(/\| \*\*(.+?)\*\* \| (.+?) \|\n/);
    assert.ok(rowMatch, "expected to find a screens table row");
    // Raw newline must not appear inside the cell content (row is a single line).
    assert.ok(!rowMatch[0].includes("\r"));
    // Pipe must be escaped, not raw within the cell text.
    assert.ok(rowMatch[1].includes("Weird \\| Screen Name"));
    assert.ok(rowMatch[2].includes("Does A \\| B and C D"));
    // The escaped pipe should NOT be interpretable as a raw column-separator pipe.
    assert.ok(!/Weird \|(?!\\) Screen/.test(md.replace(/\\\|/g, "")));
  });

  test("pipes and newlines in data model entity/field name/type/notes are escaped", () => {
    const spec = clone(validSpec());
    spec.data_model = [
      {
        entity: "Weird | Entity\nName",
        fields: [
          {
            name: "field | name\nhere",
            type: "type | here\r\nmulti",
            notes: "notes\rwith | pipe",
          },
        ],
      },
    ];
    const md = renderPrd(spec);
    const dataModelSection = md.slice(md.indexOf("## Data Model"));

    // Entity heading line.
    const headingMatch = dataModelSection.match(/#### (.+)\n/);
    assert.ok(headingMatch);
    assert.equal(headingMatch[1], "Weird \\| Entity Name");
    assert.ok(!headingMatch[0].includes("\n\n")); // single collapsed line before trailing \n

    // Field row.
    const rowMatch = dataModelSection.match(/\| `(.+?)` \| (.+?) \| (.+?) \|\n/);
    assert.ok(rowMatch, "expected to find a data model table row");
    assert.equal(rowMatch[1], "field \\| name here");
    assert.equal(rowMatch[2], "type \\| here multi");
    assert.equal(rowMatch[3], "notes with \\| pipe");

    // No raw \r remains anywhere.
    assert.ok(!md.includes("\r"));
  });

  test("the table row count matches number of screens even with embedded newlines", () => {
    const spec = clone(validSpec());
    spec.screens = [
      { name: "A\nB", purpose: "C\nD" },
      { name: "E", purpose: "F" },
    ];
    const md = renderPrd(spec);
    const section = md.slice(md.indexOf("## Screens & Navigation"));
    const lines = section.split("\n").filter((l) => l.startsWith("| **"));
    assert.equal(lines.length, 2);
  });
});

describe("renderPrd - determinism", () => {
  test("calling renderPrd twice on the same input yields identical strings", () => {
    const spec = validSpec();
    const first = renderPrd(clone(spec));
    const second = renderPrd(clone(spec));
    assert.equal(first, second);
  });

  test("determinism holds for minimalValidSpec too", () => {
    const spec = minimalValidSpec();
    const first = renderPrd(clone(spec));
    const second = renderPrd(clone(spec));
    assert.equal(first, second);
  });
});

